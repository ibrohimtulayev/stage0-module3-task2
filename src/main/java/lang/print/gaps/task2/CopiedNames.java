package lang.print.gaps.task2;
public class CopiedNames {
    public static void main(String[] args) {
        int variable1 = 0; // Changed variable name from variable to variable1
        int variable2 = 2000; // Changed variable name from variable to variable2
        int value1 = 1000; // Changed variable name from value to value1
        int variable3 = 4000; // Changed variable name from variable to variable3
        int value2 = 3000; // Changed variable name from value to value2
        int variables = 5000; // Changed variable name from variables to variables
        variables = 7000; // No change, as variables is a valid variable name
        int value3 = 90000; // Changed variable name from value to value3
        int intValue = 100000; // Changed variable name from int to intValue
        int myInteger = 1000000; // Changed variable name from my integer to myInteger
        int myVar = 1234567; // Changed variable name from my+var to myVar
        int myInteger2 = 1234567; // Changed variable name from my__-__integer to myInteger2
    }
}
