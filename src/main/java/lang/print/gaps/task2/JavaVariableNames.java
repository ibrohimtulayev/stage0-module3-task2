package lang.print.gaps.task2;

public class JavaVariableNames {
    public static void main(String[] args) {
        int m2orrow = 0; // Changed variable name from 2morrow to m2orrow
        int money = 10; // Changed variable name from $money to money
        int hundred = 100; // Changed variable name from %hundred to hundred
        int back = 1000; // Changed variable name from !back to back
        int underscore = 10000; // Changed variable name from _underscore to underscore
        int sharp = 100000; // Changed variable name from #sharp to sharp
        int star = 1000000; // Changed variable name from *star to star
        int badExampleOfVarName = 1000000; // Changed variable name from BADEXAMPLEOFVARNAME to badExampleOfVarName
    }
}

